export function getUniversityRegion(university?: string | null): UniversityRegion {
  const normalized = university?.trim();
  if (!normalized) return "その他・未登録";

  if (normalized.includes("福岡")) return "九州・沖縄";

  for (const [name, region] of Object.entries(REGION_BY_UNIVERSITY)) {
    if (normalized.includes(name)) return region;
  }

  return "その他・未登録";
}
