export const ALL_EVENTS: GameEvent[] = [...EVENTS, ...PATH_EVENTS];

export function getEventsByStage(stage: GameEvent["stage"]): GameEvent[] {
  return EVENTS.filter((e) => e.stage === stage);
}

export function getRandomEvent(stage: GameEvent["stage"], careerPath?: CareerPath | null): GameEvent {
  if (careerPath && Math.random() < 0.65) {
    const pathPool = ALL_EVENTS.filter(
      (e) => e.stage === stage && e.preferredPaths?.includes(careerPath)
    );
    if (pathPool.length > 0) {
      return pathPool[Math.floor(Math.random() * pathPool.length)];
    }
  }
  const pool = getEventsByStage(stage);
  return pool[Math.floor(Math.random() * pool.length)];
}
